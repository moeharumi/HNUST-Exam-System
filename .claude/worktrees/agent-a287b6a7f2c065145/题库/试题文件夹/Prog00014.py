p=[]
for i in range(10):
    x=eval(input())
#**********FOUND**********
    p. insert(x)
#**********FOUND**********
ma=0
mi=p[0]
#**********FOUND**********
for i in range(1,9):
    if p[i]>ma:
        ma=p[i]
    if p[i]<mi:
        mi=p[i]
print("max=", ma,"min=", mi)