import math

def is_prime(n):
#**********FOUND**********
    for i in range(1,math.sqrt(n)):
#**********FOUND**********
        if n//i==0:
            return False
    else:
        return True

def main():
#**********FOUND**********
    primes = [i for i in range(1,100) if is_prime(i)]
    print(primes)

if __name__ == '__main__':
    main()
