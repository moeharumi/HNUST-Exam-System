n=int(input('输入数据个数:'))
x=[]
for i in range(n):
    a=int(input('输入一个数:'))
    x. append(a)
#**********FOUND**********
for i in range(n/2):
#**********FOUND**********
    x[i],x[n-i]=x[n-i],x[i]
print('逆序为:',x)